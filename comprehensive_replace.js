// 安全性：文件操作函数，增加错误处理
function safeReadFile(filePath) {
    try {
        if (!fs.existsSync(filePath)) {
            throw new AppError('FILE_NOT_FOUND', `File not found: ${filePath}`);
        }
        return fs.readFileSync(filePath, 'utf8');
    } catch (error) {
        if (error instanceof AppError) throw error;
        throw new AppError('FILE_READ_ERROR', `Failed to read file: ${error.message}`);
    }
}

// 安全性：文件写入函数，带备份机制
function safeWriteFile(filePath, content) {
    try {
        // 创建备份
        if (fs.existsSync(filePath)) {
            fs.copyFileSync(filePath, BACKUP_PATH);
            console.log(`✅ 备份创建成功: ${BACKUP_PATH}`);
        }
        
        // 写入文件，指定UTF-8编码避免Windows乱码
        fs.writeFileSync(filePath, content, 'utf8');
        console.log(`✅ 文件写入成功: ${filePath}`);
    } catch (error) {
        throw new AppError('FILE_WRITE_ERROR', `Failed to write file: ${error.message}`);
    }
}

// 性能优化：预编译正则表达式
function createOptimizedReplacer(mapping) {
    const replacements = [];
    Object.entries(mapping).forEach(([english, chinese]) => {
        const pattern = `"ShortName": "${english}"`;
        const replacement = `"ShortName": "${chinese}"`;
        // 转义特殊字符
        const escapedPattern = pattern.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
        replacements.push({
            regex: new RegExp(escapedPattern, 'g'),
            replacement,
            english,
            chinese
        });
    });
    return replacements;
}

// 主要执行函数
function executeReplacement() {
    const startTime = Date.now();
    console.log('🚀 开始执行机场名称替换任务...\n');

    try {
        // 读取文件
        console.log(`📖 正在读取文件: ${FILE_PATH}`);
        let content = safeReadFile(FILE_PATH);
        
        console.log(`📊 文件读取成功，内容长度: ${content.length} 字符`);
        
        // 性能优化：预编译正则表达式
        const replacers = createOptimizedReplacer(airportNamesMap);
        console.log(`🔧 已预编译 ${replacers.length} 个正则表达式`);
        
        let replaceCount = 0;
        const replacementLog = [];

        // 批量替换
        replacers.forEach(({ regex, replacement, english, chinese }, index) => {
            const matches = content.match(regex);
            if (matches) {
                content = content.replace(regex, replacement);
                const count = matches.length;
                console.log(`✅ 替换 ${count} 个: ${english} -> ${chinese}`);
                replaceCount += count;
                replacementLog.push({ english, chinese, count });
            } else if (index < 5) {
                // 显示前5个没有匹配的项，用于调试
                console.log(`🔍 调试: ${english} 未找到匹配项`);
            }
        });

        console.log(`\n📈 总替换操作: ${replaceCount} 个`);

        // 保存文件
        safeWriteFile(FILE_PATH, content);

        // 性能统计
        const endTime = Date.now();
        const duration = endTime - startTime;

        // 检查剩余英文名称
        const remainingEnglish = content.match(/"ShortName": "[A-Za-z]/g);
        const remainingCount = remainingEnglish ? remainingEnglish.length : 0;

        console.log('\n📊 处理完成统计:');
        console.log(`⏱️  执行时间: ${duration}ms`);
        console.log(`🔍 检查映射关系: ${Object.keys(airportNamesMap).length} 个`);
        console.log(`✅ 成功替换: ${replaceCount} 个英文名称`);
        console.log(`📋 替换类型: ${replacementLog.length} 种不同名称`);
        console.log(`⚠️  剩余英文名称: ${remainingCount} 个`);
        
        if (remainingCount > 0) {
            console.log('\n💡 提示: 如需继续替换剩余的英文名称，请扩展airportNamesMap映射关系');
            // 显示前10个剩余的英文名称样例
            if (remainingEnglish) {
                console.log('\n📝 剩余英文名称样例:');
                remainingEnglish.slice(0, 10).forEach((match, i) => {
                    console.log(`   ${i + 1}. ${match}`);
                });
            }
        }

        return {
            success: true,
            replaceCount,
            remainingCount,
            duration,
            replacementLog
        };

    } catch (error) {
        if (error instanceof AppError && error.isOperational) {
            console.error(`❌ 操作错误: ${error.message}`);
        } else {
            console.error(`💥 未预期错误: ${error.message}`);
            console.error(error.stack);
        }
        
        // 尝试恢复备份
        if (fs.existsSync(BACKUP_PATH)) {
            try {
                fs.copyFileSync(BACKUP_PATH, FILE_PATH);
                console.log(`🔄 已从备份恢复文件: ${FILE_PATH}`);
            } catch (restoreError) {
                console.error(`❌ 备份恢复失败: ${restoreError.message}`);
            }
        }
        
        return { success: false, error: error.message };
    }
}

